    public void KiemTraDauVaoY(String nhietDo_x, JComboBox cb1) {
        String A[] = new String[9];
        int i = 0;
        TreeMap<Double, String> treeMap = new TreeMap<>();

        double x = hedgeAlgebra.getValue(nhietDo_x);
        for (String key : hedgeAlgebra.getHashMap().keySet()) {
            if (hedgeAlgebra.getHashMap().get(key) <= 1.0 - x) {
                treeMap.put(hedgeAlgebra.getHashMap().get(key), key);
            }
        }

        for (String value : treeMap.values()) {
            A[i++] = value;
        }
        cb1.setModel(new javax.swing.DefaultComboBoxModel<>(A));
    }

    public void KiemTraDauVaoZ(String nhietDo_x, String nhietDo_y, JComboBox cb2) {
        String A[] = new String[9];
        int i = 0;
        TreeMap<Double, String> treeMap = new TreeMap<>();

        double x = hedgeAlgebra.getValue(nhietDo_x);
        double y = hedgeAlgebra.getValue(nhietDo_y);
        for (String key : hedgeAlgebra.getHashMap().keySet()) {
            if (hedgeAlgebra.getHashMap().get(key) <= 1.0 - x - y) {
                treeMap.put(hedgeAlgebra.getHashMap().get(key), key);
            }
        }

        for (String value : treeMap.values()) {
            A[i++] = value;

        }
        cb2.setModel(new javax.swing.DefaultComboBoxModel<>(A));
    }

